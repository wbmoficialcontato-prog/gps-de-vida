module.exports = async function handler(req, res) {
  if (req.method === 'OPTIONS') {
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
    return res.status(200).end();
  }

  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' });
  }

  try {
    const { messages, nome, palavraChave } = req.body;

    const SUPABASE_URL = process.env.SUPABASE_URL;
    const SUPABASE_KEY = process.env.SUPABASE_SECRET_KEY;

    // Buscar histórico existente
    let historico = [];
    if (nome && palavraChave) {
      const busca = await fetch(
        `${SUPABASE_URL}/rest/v1/conversas?nome=eq.${encodeURIComponent(nome)}&palavra_chave=eq.${encodeURIComponent(palavraChave)}&select=id,historico`,
        { headers: { 'apikey': SUPABASE_KEY, 'Authorization': `Bearer ${SUPABASE_KEY}` } }
      );
      const dados = await busca.json();
      if (dados && dados.length > 0) {
        historico = dados[0].historico || [];
      }
    }

    // Combinar histórico salvo com mensagens da sessão atual
    const todasMensagens = [...historico, ...messages];

    // Chamar Anthropic
    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': process.env.ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01'
      },
      body: JSON.stringify({
        model: 'claude-haiku-4-5-20251001',
        max_tokens: 1000,
        system: SYSTEM_PROMPT,
        messages: todasMensagens
      })
    });

    const data = await response.json();
    const resposta = data.content?.[0]?.text || '';

    // Salvar histórico atualizado no Supabase
    if (nome && palavraChave && resposta) {
      const novoHistorico = [
        ...todasMensagens,
        { role: 'assistant', content: resposta }
      ];

      // Limitar a 50 mensagens para não crescer demais
      const historicoLimitado = novoHistorico.slice(-50);

      const buscaId = await fetch(
        `${SUPABASE_URL}/rest/v1/conversas?nome=eq.${encodeURIComponent(nome)}&palavra_chave=eq.${encodeURIComponent(palavraChave)}&select=id`,
        { headers: { 'apikey': SUPABASE_KEY, 'Authorization': `Bearer ${SUPABASE_KEY}` } }
      );
      const dadosId = await buscaId.json();

      if (dadosId && dadosId.length > 0) {
        // Atualizar existente
        await fetch(
          `${SUPABASE_URL}/rest/v1/conversas?id=eq.${dadosId[0].id}`,
          {
            method: 'PATCH',
            headers: {
              'apikey': SUPABASE_KEY,
              'Authorization': `Bearer ${SUPABASE_KEY}`,
              'Content-Type': 'application/json'
            },
            body: JSON.stringify({ historico: historicoLimitado, atualizado_em: new Date().toISOString() })
          }
        );
      } else {
        // Criar novo
        await fetch(
          `${SUPABASE_URL}/rest/v1/conversas`,
          {
            method: 'POST',
            headers: {
              'apikey': SUPABASE_KEY,
              'Authorization': `Bearer ${SUPABASE_KEY}`,
              'Content-Type': 'application/json'
            },
            body: JSON.stringify({ nome, palavra_chave: palavraChave, historico: historicoLimitado })
          }
        );
      }
    }

    res.setHeader('Access-Control-Allow-Origin', '*');
    return res.status(200).json(data);
  } catch (err) {
    return res.status(500).json({ error: err.message });
  }
}
