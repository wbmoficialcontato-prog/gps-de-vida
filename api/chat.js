module.exports = async function handler(req, res) {
  if (req.method === 'OPTIONS') {
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
    return res.status(200).end();
  }

  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' });
  }

  try {
    const { messages, nome, palavraChave } = req.body;

    const SUPABASE_URL = process.env.SUPABASE_URL;
    const SUPABASE_KEY = process.env.SUPABASE_SECRET_KEY;

    let historicoSalvo = [];

    if (nome && palavraChave && SUPABASE_URL && SUPABASE_KEY) {
      try {
        const busca = await fetch(
          `${SUPABASE_URL}/rest/v1/conversas?nome=eq.${encodeURIComponent(nome)}&palavra_chave=eq.${encodeURIComponent(palavraChave)}&select=id,historico`,
          { headers: { 'apikey': SUPABASE_KEY, 'Authorization': `Bearer ${SUPABASE_KEY}` } }
        );
        const dados = await busca.json();
        if (dados && dados.length > 0 && dados[0].historico) {
          historicoSalvo = dados[0].historico;
        }
      } catch (e) {
        console.error('Erro ao buscar historico:', e.message);
      }
    }

    const todasMensagens = historicoSalvo.length > 0 && messages.length === 1
      ? [...historicoSalvo, ...messages]
      : messages;

    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': process.env.ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01'
      },
      body: JSON.stringify({
        model: 'claude-haiku-4-5-20251001',
        max_tokens: 1000,
        system: SYSTEM_PROMPT,
        messages: todasMensagens
      })
    });

    const data = await response.json();
    const resposta = data.content?.[0]?.text || '';

    if (nome && palavraChave && resposta && SUPABASE_URL && SUPABASE_KEY) {
      try {
        const novoHistorico = [...todasMensagens, { role: 'assistant', content: resposta }].slice(-50);

        const buscaId = await fetch(
          `${SUPABASE_URL}/rest/v1/conversas?nome=eq.${encodeURIComponent(nome)}&palavra_chave=eq.${encodeURIComponent(palavraChave)}&select=id`,
          { headers: { 'apikey': SUPABASE_KEY, 'Authorization': `Bearer ${SUPABASE_KEY}` } }
        );
        const dadosId = await buscaId.json();

        if (dadosId && dadosId.length > 0) {
          await fetch(
            `${SUPABASE_URL}/rest/v1/conversas?id=eq.${dadosId[0].id}`,
            {
              method: 'PATCH',
              headers: { 'apikey': SUPABASE_KEY, 'Authorization': `Bearer ${SUPABASE_KEY}`, 'Content-Type': 'application/json' },
              body: JSON.stringify({ historico: novoHistorico, atualizado_em: new Date().toISOString() })
            }
          );
        } else {
          await fetch(
            `${SUPABASE_URL}/rest/v1/conversas`,
            {
              method: 'POST',
              headers: { 'apikey': SUPABASE_KEY, 'Authorization': `Bearer ${SUPABASE_KEY}`, 'Content-Type': 'application/json' },
              body: JSON.stringify({ nome, palavra_chave: palavraChave, historico: novoHistorico })
            }
          );
        }
      } catch (e) {
        console.error('Erro ao salvar historico:', e.message);
      }
    }

    res.setHeader('Access-Control-Allow-Origin', '*');
    return res.status(200).json(data);

  } catch (err) {
    return res.status(500).json({ error: err.message });
  }
};
